package day01.pojo;

public class User {
    private Integer id;
    private String name;
    private String exp;

    public Integer getUserId() {
        return id;
    }

    public void setUserId(Integer id) {
        this.id = id;
    }

    public String getUserName() {
        return name;
    }

    public void setUserName(String name) {
        this.name = name;
    }

    public String getUserExp() {
        return exp;
    }

    public void setUserExp(String exp) {
        this.exp = exp;
    }
}
